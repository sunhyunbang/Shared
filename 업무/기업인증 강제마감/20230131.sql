AND DATEDIFF(D,'2023-02-06 00:00:00.000' ,AD.END_DT) >= 0
AND AD.GROUP_CD in (14) -- 구인
AND AD.VERSION IN ('E','M') -- 등록대행,E-COMM만(신문 제외)
AND AD.ENDYN = 'N'  
AND AD.DELYN = 'N'


