-- 2. 강제 마감 (커서)

--변수 선언
DECLARE 
@INDEX INT,
@LINEADNO NVARCHAR(16),
@LINEADID INT,
@INPUT_BRANCH INT,
@LAYOUT_BRANCH INT

SET @INDEX = 0

DECLARE CUR CURSOR FOR

SELECT
LINEADNO ,
LINEADID ,
INPUT_BRANCH ,
LAYOUT_BRANCH 
FROM #TMP_TB

OPEN CUR --커서 오픈
FETCH NEXT FROM CUR INTO @LINEADNO , @LINEADID , @INPUT_BRANCH ,@LAYOUT_BRANCH  --SELECT 한 값을 변수에 넣음

--커서이용해 한 ROW 씩 읽음
WHILE @@FETCH_STATUS = 0
BEGIN
SET @INDEX = @INDEX + 1

UPDATE PP_AD_TB  
SET ENDYN = 'Y'  
    ,END_DT = GETDATE()  
    ,MOD_DT = GETDATE()  
WHERE LINEADNO = @LINEADNO

EXECUTE p_SyncIssueData @LINEADID, @INPUT_BRANCH, @LAYOUT_BRANCH

FETCH NEXT FROM CUR INTO @LINEADNO , @LINEADID , @INPUT_BRANCH ,@LAYOUT_BRANCH
END

--커서 닫고 초기화
CLOSE CUR
DEALLOCATE CUR